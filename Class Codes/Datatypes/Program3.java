class Program{
	public static void main(String[] args){
		float f1 = 7.5;  // 7.5f
		float f2 = 7.5;  // 7.5f

		System.out.println(f1);
		System.out.println(f2);

		// By default Java consider a decimal value as of type 'double' to clearify it as 'float' we need to put 'f' in front of the value
	}
}
