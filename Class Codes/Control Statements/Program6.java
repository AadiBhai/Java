// Given an Integer input, print whether its odd or even
// Input: 7; Output: 7 is odd
// Input: 4; Output: 4 is even

class Demo{
	public static void main(String[] args){
		int x = 7;

		if(x%2 == 0){
			System.out.println(x + " is even");
		} else {
			System.out.println(x + " is odd");
		}
	}
}
